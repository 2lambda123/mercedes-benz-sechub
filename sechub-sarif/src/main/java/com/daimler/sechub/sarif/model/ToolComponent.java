package com.daimler.sechub.sarif.model;

public class ToolComponent {

    private String name;
    private String guid;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setGuid(String guid) {
        this.guid = guid;
    }

    public String getGuid() {
        return guid;
    }
}
